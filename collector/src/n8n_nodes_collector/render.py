def sort_index_map(index_map: dict[str, list[str]]) -> dict[str, list[str]]:
    return {key: sorted(values) for key, values in sorted(index_map.items())}


def bullet_block(items: list[str]) -> str:
    return "\n".join(f"- {item}" for item in items) if items else "- Not present in source."


def write_json(path: Path, payload: object) -> None:
    path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")


def write_text(path: Path, content: str) -> None:
    path.write_text(content if content.endswith("\n") else f"{content}\n", encoding="utf-8")
